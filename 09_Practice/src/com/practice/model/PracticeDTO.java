package com.practice.model;

public class PracticeDTO {
	
	private int practice_no;
	private String practice_writer;
	private String practice_title;
	private String practice_cont;
	private String practice_pwd;
	private int practice_hit;
	private String practice_regdate;
	private String practice_head;
	

	public int getPractice_no() {
		return practice_no;
	}
	public void setPractice_no(int practice_no) {
		this.practice_no = practice_no;
	}
	public String getPractice_writer() {
		return practice_writer;
	}
	public void setPractice_writer(String practice_writer) {
		this.practice_writer = practice_writer;
	}
	public String getPractice_title() {
		return practice_title;
	}
	public void setPractice_title(String practice_title) {
		this.practice_title = practice_title;
	}
	public String getPractice_cont() {
		return practice_cont;
	}
	public void setPractice_cont(String practice_cont) {
		this.practice_cont = practice_cont;
	}
	public String getPractice_pwd() {
		return practice_pwd;
	}
	public void setPractice_pwd(String practice_pwd) {
		this.practice_pwd = practice_pwd;
	}
	public int getPractice_hit() {
		return practice_hit;
	}
	public void setPractice_hit(int practice_hit) {
		this.practice_hit = practice_hit;
	}
	public String getPractice_regdate() {
		return practice_regdate;
	}
	public void setPractice_regdate(String practice_regdate) {
		this.practice_regdate = practice_regdate;
	}
	public String getPractice_head() {
		return practice_head;
	}
	public void setPractice_head(String practice_head) {
		this.practice_head = practice_head;
	}
	
	
	

}
